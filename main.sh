#!/bin/bash

function typewriter
{
    text=" .----------------.  .----------------.  .----------------. 
| .--------------. || .--------------. || .--------------. |
| | _____  _____ | || |  _________   | || |   ______     | |
| ||_   _||_   _|| || | |_   ___  |  | || |  |_   _ \    | |
| |  | | /\ | |  | || |   | |_  \_|  | || |    | |_) |   | |
| |  | |/  \| |  | || |   |  _|  _   | || |    |  __'.   | |
| |  |   /\   |  | || |  _| |___/ |  | || |   _| |__) |  | |
| |  |__/  \__|  | || | |_________|  | || |  |_______/   | |
| |              | || |              | || |              | |
| '--------------' || '--------------' || '--------------' |
 '----------------'  '----------------'  '----------------' "
    delay="0.01"

    for i in $(seq 0 $(expr length "${text}")) ; do
        echo -n "${text:$i:1}"
        sleep ${delay}
    done
}
typewriter
echo
echo " .----------------.  .----------------.  .----------------. 
| .--------------. || .--------------. || .--------------. |
| | _____  _____ | || |  _________   | || |   ______     | |
| ||_   _||_   _|| || | |_   ___  |  | || |  |_   _ \    | |
| |  | | /\ | |  | || |   | |_  \_|  | || |    | |_) |   | |
| |  | |/  \| |  | || |   |  _|  _   | || |    |  __'.   | |
| |  |   /\   |  | || |  _| |___/ |  | || |   _| |__) |  | |
| |  |__/  \__|  | || | |_________|  | || |  |_______/   | |
| |              | || |              | || |              | |
| '--------------' || '--------------' || '--------------' |
 '----------------'  '----------------'  '----------------' " >> out
echo "Enter the name of Domain list file:"
read filename
while read line
do
s="curl -s -o /dev/null -w "%{http_code}" https://$line"
p="curl -s -o /dev/null -w "%{http_code}" http://$line"
echo $s
reshttps="$(eval $s)"
echo "HTTPS Status code = $reshttps"
reshttp="$(eval $p)"
echo "HTTP Status code = $reshttp"
echo "-------------------------------------"
if [ $((reshttps)) -lt 399 ]
then
echo "https://$line --->  Status code= $reshttps" >> out
echo "-------------------------------------" >> out
elif [ $((reshttp)) -lt 399 ]
then
echo "https://$line --->  Status code= $reshttp" >> out
echo "-------------------------------------" >> out
else
echo "$line --->  Status code= ERROR" >> out
echo "-------------------------------------" >> out
fi
done < $filename
